#!/usr/bin/env node
/**
 * forward-ref-audit — find "forward-reference" defects: a person/place/concept/
 * proper-noun named in the river BEFORE it is established, surfaced after the
 * Move 2 reorder pulled chapters forward.
 *
 * Two passes, because key entities (e.g. Törbel) are untagged and live only in prose:
 *   A. TAG pass     — significant tags (person/place/concept/image/motif) whose FIRST
 *                     river occurrence is in a moved-earlier chapter and that recur later.
 *   B. PROPER-NOUN  — capitalized proper nouns/bigrams in gesture bodies, same test.
 *
 * Heuristic → emits CANDIDATES for human triage. Output: analysis/forward-ref-audit.md
 * Usage: node scripts/forward-ref-audit.mjs
 */
import fs from "node:fs";
import path from "node:path";
import url from "node:url";
import yaml from "js-yaml";
import { loadCorpus } from "../src/lib/load-corpus.mjs";

const ROOT = path.resolve(path.dirname(url.fileURLToPath(import.meta.url)), "..");

// Chapters Move 2 pulled earlier — where forward-refs concentrate.
const MOVED_EARLIER = new Set([
  "even-that-part", "the-hollow-book", "reenchanting",
  "angel-faces-backward", "wizards-walk",
]);

const SIGNIFICANT = ["image/", "place/", "person/", "motif/", "concept/"];
const DENY = new Set(["concept/commons", "concept/community", "concept/practice"]);
const isSignificant = t => SIGNIFICANT.some(p => t.startsWith(p)) && !DENY.has(t);

const corpus = loadCorpus(ROOT);
const river = yaml.load(fs.readFileSync(path.join(ROOT, "src/content/manifests/the-river.manifest.yml"), "utf8"));
const bySlug = Object.fromEntries(corpus.plateaus.map(p => [p.slug, p]));

// Flatten the river into ordered units: { gid, slug, pos }.
const units = [];
river.sources.forEach(slug => {
  const ch = bySlug[slug];
  if (!ch) return;
  for (const gid of ch.gestures) units.push({ gid, slug, pos: units.length });
});
const posChapter = slug => river.sources.indexOf(slug);

// ---- Pass A: tags ----
const tagOcc = new Map(); // tag -> [{gid, slug, pos}]
for (const u of units) {
  const g = corpus.gestures[u.gid];
  if (!g) continue;
  for (const t of g.tags) {
    if (!isSignificant(t)) continue;
    if (!tagOcc.has(t)) tagOcc.set(t, []);
    tagOcc.get(t).push(u);
  }
}
const tagFlags = [];
for (const [tag, occ] of tagOcc) {
  if (occ.length < 2) continue;
  const first = occ[0];
  if (!MOVED_EARLIER.has(first.slug)) continue;
  const later = occ.slice(1).filter(o => !MOVED_EARLIER.has(o.slug));
  if (!later.length) continue;
  tagFlags.push({ tag, first, later });
}

// ---- Pass B: proper nouns ----
const STOP = new Set(("The This That These Those There Their They Them Then Than And But Or Nor So Yet For Not " +
  "When Where What Why How Who Whom Whose If Now Here We You Your Our His Her Its It He She " +
  "A An As At By In On Of To Up Do Did Does Is Are Was Were Be Been Being Have Has Had " +
  "I We Most Some Many Each Every All No One Two Three Part Chapter Like Even Once After Before " +
  "Walk Consider Take Look Start Begin Continue Yes Maybe Still And Yet Because Until While").split(/\s+/));

const phraseOcc = new Map(); // phrase -> [{gid, slug, pos, snippet, midSentence}]
const PHRASE = /\b([A-ZÀ-Þ][a-zà-ÿ'’]+(?:\s+(?:of|the|and|für)\s+)?(?:[A-ZÀ-Þ][a-zà-ÿ'’]+)*)\b/g;
for (const u of units) {
  const g = corpus.gestures[u.gid];
  if (!g) continue;
  const body = g.body.normalize("NFC"); // precompose accents (ö etc.) so the regex sees them
  let m;
  while ((m = PHRASE.exec(body)) !== null) {
    const phrase = m[1].trim();
    const head = phrase.split(/\s+/)[0];
    if (STOP.has(head)) continue;
    if (phrase.length < 4) continue;
    // sentence-initial? preceding non-space char is . ! ? : — or start/newline
    const before = body.slice(0, m.index).replace(/\s+$/, "");
    const midSentence = before.length > 0 && !/[.!?:—\n]$/.test(before);
    if (!phraseOcc.has(phrase)) phraseOcc.set(phrase, []);
    phraseOcc.get(phrase).push({
      ...u, midSentence,
      snippet: body.slice(Math.max(0, m.index - 40), m.index + phrase.length + 40).replace(/\s+/g, " ").trim(),
    });
  }
}
const nounFlags = [];
for (const [phrase, occ] of phraseOcc) {
  if (occ.length < 2) continue;
  const first = occ[0];
  if (!MOVED_EARLIER.has(first.slug)) continue;
  // must look like a real proper noun: appears mid-sentence at least once
  if (!occ.some(o => o.midSentence)) continue;
  const later = occ.slice(1).filter(o => !MOVED_EARLIER.has(o.slug));
  if (!later.length) continue;
  nounFlags.push({ phrase, first, later });
}
nounFlags.sort((a, b) => (b.later.length - a.later.length) || a.first.pos - b.first.pos);

// ---- Report ----
const L = [];
L.push("# Forward-reference audit (generated by scripts/forward-ref-audit.mjs)\n");
L.push("Candidates: a person/place/concept/proper-noun whose FIRST river appearance is in a");
L.push("Move-2 moved-earlier chapter (even-that-part, the-hollow-book, reenchanting,");
L.push("angel-faces-backward, wizards-walk) and that recurs in a later, non-moved chapter —");
L.push("i.e. possibly named before it is established. **Heuristic; triage before acting.**\n");

L.push(`## A. Tag pass (${tagFlags.length})\n`);
for (const f of tagFlags.sort((a, b) => a.first.pos - b.first.pos)) {
  const laterStr = f.later.map(o => `${o.slug}@${posChapter(o.slug)}`).join(", ");
  L.push(`- **${f.tag}** — first: ${f.first.gid} (${f.first.slug} @ch${posChapter(f.first.slug)}) · later: ${laterStr}`);
}
L.push(`\n## B. Proper-noun pass (${nounFlags.length})\n`);
for (const f of nounFlags) {
  const laterStr = f.later.map(o => `${o.slug}@${posChapter(o.slug)}`).join(", ");
  L.push(`- **${f.phrase}** — first: ${f.first.gid} (${f.first.slug} @ch${posChapter(f.first.slug)}), later: ${laterStr}`);
  L.push(`  - first snippet: …${f.first.snippet}…`);
}

const dest = path.join(ROOT, "analysis/forward-ref-audit.md");
fs.writeFileSync(dest, L.join("\n") + "\n");
console.error(`tag candidates: ${tagFlags.length} · proper-noun candidates: ${nounFlags.length} -> ${path.relative(ROOT, dest)}`);
