// gen-merge-map-doc.mjs — render analysis/merge-map.json into a readable
// markdown working document grouped by plateau. STRONG merges surfaced first.
//
//   node scripts/gen-merge-map-doc.mjs   →  analysis/merge-map.md

import fs from "node:fs";
import path from "node:path";
import url from "node:url";
import { loadCorpus } from "../src/lib/load-corpus.mjs";

const __dirname = path.dirname(url.fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(__dirname, "..");
const data = JSON.parse(fs.readFileSync(path.join(REPO_ROOT, "analysis", "merge-map.json"), "utf8"));
const { gestures: allGestures } = loadCorpus(REPO_ROOT);
const gestureTotal = Object.keys(allGestures).length;

// Group by plateau, preserving book order of pairs.
const byPlateau = new Map();
for (const p of data.pairs) {
  if (!byPlateau.has(p.plateau)) byPlateau.set(p.plateau, []);
  byPlateau.get(p.plateau).push(p);
}

const lines = [];
lines.push("# Merge map — first-pass gesture corpus");
lines.push("");
lines.push("Advisory. Proposes which **paragraph-adjacent** gestures are likely one");
lines.push("continuous arc the mechanical split fragmented. Section-separated pairs are");
lines.push("excluded — the `---` is authorial intent to hold apart.");
lines.push("");
lines.push("Generated by `scripts/merge-map.mjs` → `gen-merge-map-doc.mjs`. Regenerate:");
lines.push("`node scripts/merge-map.mjs --json && node scripts/gen-merge-map-doc.mjs`.");
lines.push("");
lines.push("**Bands:** `STRONG` (score ≥ 3, high confidence) · `consider` (1–2) · `keep` (≤ 0).");
lines.push("Take STRONG with light review; weigh `consider` case by case; `keep` left out.");
lines.push("");
lines.push("**To merge a pair** `G-XX-AAA → G-XX-BBB`: append B's body to A's (blank line");
lines.push("between), delete B's file, remove B's id and the paragraph separator at that");
lines.push("index from the plateau manifest. A's id is kept; B's id is retired. Re-run");
lines.push("`compile-plateau verify` against the snapshot — a merge is byte-preserving");
lines.push("(it only removes a `\\n\\n` join, which the composer re-adds), so the snapshot");
lines.push("check still passes.");
lines.push("");

// Corpus totals
const strongAll = data.pairs.filter(p => p.band === "STRONG").length;
const considerAll = data.pairs.filter(p => p.band === "consider").length;
lines.push("## Corpus summary");
lines.push("");
lines.push(`- ${gestureTotal} gestures across ${byPlateau.size} plateaus, ${data.pairs.length} paragraph adjacencies scored.`);
lines.push(`- **${strongAll} STRONG** merges → if all taken, ${gestureTotal} → ${gestureTotal - strongAll} gestures (${Math.round(100 * strongAll / gestureTotal)}% reduction).`);
lines.push(`- ${considerAll} *consider* merges available beyond that (review individually).`);
lines.push("");

// Per-plateau pressure table
lines.push("## Per-plateau merge pressure");
lines.push("");
lines.push("| plateau | adjacencies | STRONG | consider | keep |");
lines.push("|---|---|---|---|---|");
for (const [pid, pairs] of byPlateau) {
  const s = pairs.filter(p => p.band === "STRONG").length;
  const c = pairs.filter(p => p.band === "consider").length;
  const k = pairs.filter(p => p.band === "keep").length;
  lines.push(`| ${pid} | ${pairs.length} | ${s} | ${c} | ${k} |`);
}
lines.push("");

// Per-plateau detail — STRONG first, then consider.
for (const [pid, pairs] of byPlateau) {
  const strong = pairs.filter(p => p.band === "STRONG");
  const consider = pairs.filter(p => p.band === "consider");
  if (strong.length === 0 && consider.length === 0) continue;

  lines.push(`## ${pid} — ${pairs[0].slug}`);
  lines.push("");

  if (strong.length) {
    lines.push(`### STRONG (${strong.length})`);
    lines.push("");
    for (const p of strong) {
      lines.push(`**${p.from} → ${p.to}** · score +${p.score} · ${p.combined}w`);
      lines.push(`> A: ${p.aPreview}…`);
      lines.push(`> B: ${p.bPreview}…`);
      lines.push(`> _${p.reasons.join(" · ")}_`);
      lines.push("");
    }
  }

  if (consider.length) {
    lines.push(`### consider (${consider.length})`);
    lines.push("");
    for (const p of consider) {
      lines.push(`- **${p.from} → ${p.to}** (+${p.score}, ${p.combined}w) — ${p.reasons.join("; ")}`);
    }
    lines.push("");
  }
}

const outPath = path.join(REPO_ROOT, "analysis", "merge-map.md");
fs.writeFileSync(outPath, lines.join("\n") + "\n");
console.log(`wrote ${path.relative(REPO_ROOT, outPath)} (${lines.length} lines)`);
