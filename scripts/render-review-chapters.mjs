#!/usr/bin/env node
/**
 * render-review-chapters — emit one human-readable markdown file per chapter,
 * in canonical river order, for line-targeted review on GitHub.
 *
 * Each file is the composed reading text (composePlateauBody output) with a
 * small header carrying the part / label / h1, so a reviewer sees the prose
 * exactly as it reads, and per-commit diffs show prose changes cleanly.
 *
 * Output: for-agents/enchantment/rendered/<NN>-<slug>.md  (NN = river position)
 *
 * Usage: node scripts/render-review-chapters.mjs
 */
import fs from "node:fs";
import path from "node:path";
import url from "node:url";
import yaml from "js-yaml";
import { composePlateauBody } from "../src/lib/composePlateauBody.mjs";

const __dirname = path.dirname(url.fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const OUT_DIR = path.join(ROOT, "for-agents/enchantment/rendered");

const man = yaml.load(
  fs.readFileSync(path.join(ROOT, "src/content/manifests/the-river.manifest.yml"), "utf8"),
);

function frontmatter(slug) {
  const text = fs.readFileSync(path.join(ROOT, "src/content/book", slug + ".md"), "utf8");
  const end = text.indexOf("\n---\n", 4);
  return yaml.load(text.slice(4, end)) || {};
}

function plain(h1) {
  return (h1 || "").replace(/<br\/?>/gi, " ").replace(/\s+/g, " ").trim();
}

fs.mkdirSync(OUT_DIR, { recursive: true });

man.sources.forEach((slug, i) => {
  const fm = frontmatter(slug);
  const body = composePlateauBody(path.join(ROOT, "src/content/book", slug + ".md"), ROOT).trim();
  const nn = String(i + 1).padStart(2, "0");
  const header = [
    `<!-- rendered for review — generated by scripts/render-review-chapters.mjs. Do not edit by hand. -->`,
    fm.part ? `**${fm.part}**` : null,
    `# ${plain(fm.h1) || slug}`,
    fm.label ? `*${fm.label}*` : null,
  ].filter(Boolean).join("\n\n");
  fs.writeFileSync(path.join(OUT_DIR, `${nn}-${slug}.md`), `${header}\n\n---\n\n${body}\n`);
});

console.error(`wrote ${man.sources.length} rendered chapters -> ${path.relative(ROOT, OUT_DIR)}/`);
